export const supportedStrokeOrderCharacters = Object.freeze(Object.keys(rawStrokeOrders))

function pointsToPath(points: number[][]) {
  return points
    .map(([x, y], index) => `${index === 0 ? 'M' : 'L'} ${x} ${y}`)
    .join(' ')
}

export function getStrokeOrder(character: string): StrokeOrderData | null {
  const rawStrokeOrder = rawStrokeOrders[character]

  if (!rawStrokeOrder) {
    return null
  }

  return {
    character,
    transform: STROKE_TRANSFORM,
    viewBox: STROKE_VIEW_BOX,
    strokes: rawStrokeOrder.strokes.map((d, index) => ({
      d,
      label: `第 ${index + 1} 笔`,
      median: pointsToPath(rawStrokeOrder.medians[index] ?? []),
    })),
  }
}
